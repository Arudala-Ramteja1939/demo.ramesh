package UnWantedClasses;

public class My {

	private Long id;
	private String name;
	private Long age;
	public My(Long id, String name, Long age) {
		super();
		this.id = id;
		this.name = name;
		this.age = age;
	}
	public Long getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public Long getAge() {
		return age;
	}
	public My() {
		super();
	}
	
	
}
